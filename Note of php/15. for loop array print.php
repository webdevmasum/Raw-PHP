<?php
    // Example array
    $numbers = [1, 2, 3, 4, 5];

    // For loop diye array print kora
    for ($i = 0; $i < count($numbers); $i++) {
        echo $numbers[$i] . " "; // array er prottekta element print hobe
    }
?>



